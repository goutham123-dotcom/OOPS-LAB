class string
{
public static void main(String [] args)
{
String s1 = "Hello";
String s2 = "How @ are @ you";
System.out.println("s1:"+s1);
System.out.println("s2:"+s2);
System.out.println("Length of S1\t"+s1.length());
System.out.println("Index of e\t"+s1.indexOf('e'));
System.out.println("Char At(2):\t"+s1.charAt(2));
System.out.println("To lower:\t"+s1.toLowerCase());
System.out.println("Substring :\t"+s2.substring(2,7));
}
}